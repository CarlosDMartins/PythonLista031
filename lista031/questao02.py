#2) Elaborar um programa que pergunte quatro valores inteiros e apresente 2 resultados:
#a) Resultado de suas adições
#b) Resultado de suas multiplicações

num1 = int(input("coloque seu primeiro número:"))
num2 = int(input("coloque seu segundo número:"))
num3 = int(input("coloque seu terceiro número:"))
num4 = int(input("coloque seu quarto número:"))

Adtotal = num1 + num2 + num3 + num4
Multotal = num1 * num2 * num3 * num4

print("O resultado da sua adição é:", Adtotal)
print("O resultado da sua multiplicação é:", Multotal)
